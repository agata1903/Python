#A função sep fará um separador
#O print a seguir separará todas as frases com uma quebra de linha

print("Meu","nome","é","Ágata", sep="\n")

#A função end fará algo que ficará no final da frase
#Abaixo, o end fará com que o print de baixo seja lido na primeira linha

print("Meu nome é Ágata.", end=" ")
print("Tenho 25 anos.")